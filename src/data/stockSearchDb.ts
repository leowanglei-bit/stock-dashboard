const marketMap: Record<number, StockSearchItem['market']> = {
  0: 'sh', 1: 'sz', 2: 'chinext', 3: 'star',
};

const FULL_DB: StockSearchItem[] = DB.map(([code, name, mkt]) => ({
  code, name, market: marketMap[mkt],
}));

export function searchStocksLocal(query: string): StockSearchItem[] {
  const q = query.trim();
  if (!q) return [];

  const results = FULL_DB.filter((s) => {
    if (s.name === q || s.code === q) return true;
    if (q.length >= 2) {
      if (s.name.includes(q) || s.code.includes(q)) return true;
    }
    return false;
  });

  // 精确匹配排前
  results.sort((a, b) => {
    const sa = a.name === q || a.code === q ? 0 : a.name.startsWith(q) ? 1 : 2;
    const sb = b.name === q || b.code === q ? 0 : b.name.startsWith(q) ? 1 : 2;
    return sa - sb;
  });

  return results.slice(0, 20);
}
